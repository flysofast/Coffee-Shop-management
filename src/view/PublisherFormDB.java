    void loadPublisherListToTable() {
        DefaultTableModel dtm = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int i, int i1) {
                return false; //To change body of generated methods, choose Tools | Templates.
            }
        };

        dtm.addColumn("ID");
        dtm.addColumn("Name");
        dtm.addColumn("Certificate");

        for (Publisher pu : pubModel.getAll()) {
            dtm.addRow(new Object[]{pu.getId(), pu.getName(), pu.getCertificate()});
        }

        tblPublisher.setModel(dtm);
        tblPublisher.getColumn("ID").setMaxWidth(36);
    }

