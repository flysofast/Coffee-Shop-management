    private void btncreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncreateActionPerformed
        // TODO add your handling code here:
        txtId.setText(" ");
        if (txtName.getText().isEmpty() && txtCer.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Publisher name or Certificate cannot be empty!");
            return;
        }

        Publisher publisher = new Publisher();
        publisher.setName(txtName.getText());
        publisher.setCertificate(txtCer.getText());
        publisher.setIsActive(true);

        if (!pubModel.addNew(publisher)) {
            JOptionPane.showMessageDialog(null, "Failed to add new Publisher!");
        } else {
            JOptionPane.showMessageDialog(null, "Publisher added successfully!");
            loadPublisherListToTable();
        }
    }//GEN-LAST:event_btncreateActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (txtName.getText().isEmpty() && txtCer.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Publisher name or Certificat cannot be empty!");
            return;
        }

        Publisher publisher = new Publisher();
        publisher.setId(Integer.valueOf(txtId.getText()));
        publisher.setName(txtName.getText());
        publisher.setCertificate(txtCer.getText());
        publisher.setIsActive(true);

        if (!pubModel.edit(publisher)) {
            JOptionPane.showMessageDialog(null, "Failed to update Publisher!");
        } else {
            JOptionPane.showMessageDialog(null, "Publisher updated successfully!");
            txtId.setText(" ");
            loadPublisherListToTable();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
        // TODO add your handling code here:
        Publisher publisher = new Publisher();
        publisher.setId(Integer.valueOf(txtId.getText()));
        publisher.setName(txtName.getText());
        publisher.setCertificate(txtCer.getText());
        pubModel.delete(publisher);
        loadPublisherListToTable();
        JOptionPane.showMessageDialog(null, "Publisher deleted successfully!");
        txtId.setText(" ");
    }//GEN-LAST:event_btnResetActionPerformed


