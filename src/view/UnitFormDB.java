    private void btncreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncreateActionPerformed
        // TODO add your handling code here:
        txtId.setText(" ");
        if (txtName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Unit name cannot be empty!");
            return;
        }

        Unit unit = new Unit(txtName.getText(), true);

        if (!unitModel.addNew(unit)) {
            JOptionPane.showMessageDialog(null, "Failed to add new Unit!");
        } else {
            JOptionPane.showMessageDialog(null, "Unit added successfully!");
            loadUnitListToTable();
        }
    }//GEN-LAST:event_btncreateActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (txtName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Unit name cannot be empty!");
            return;
        }

        Unit unit = new Unit();
        unit.setId(Integer.valueOf(txtId.getText()));
        unit.setName(txtName.getText());
        unit.setIsActive(true);

        if (!unitModel.edit(unit)) {
            JOptionPane.showMessageDialog(null, "Failed to update Unit!");
        } else {
            JOptionPane.showMessageDialog(null, "Unit updated successfully!");
            txtId.setText(" ");
            loadUnitListToTable();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
        // TODO add your handling code here:
        Unit unit = new Unit();
        unit.setId(Integer.valueOf(txtId.getText()));
        unit.setName(txtName.getText());

        unitModel.delete(unit);
        loadUnitListToTable();
        JOptionPane.showMessageDialog(null, "Unit deleted successfully!");
        txtId.setText(" ");
    }//GEN-LAST:event_btnResetActionPerformed


